<script>
    import axios from "axios";

    const {ipcRenderer} = require("electron");
    import {mapMutations} from "vuex";

    export default {
        name: "titleBlock",
        computed: {
            hurl: {
                get() {
                    return this.$store.getters.getHurl;
                },
                set(val) {
                    this.SET_HURL(val);
                },
            },
        },
        data() {
            return {
                searchForState: "off",
                searchValue: "",
                historys: [],
                hotSearchs: []
            };
        },
        created() {
            axios
                .get("/history.json").then((resp) => {
                for (var i = 0; i < resp.data["history"].length; i++) {
                    if (i < 10) {
                        this.historys.push(resp.data["history"][i]);
                    }
                }

            }).catch((err) => {
                this.$Message.error("历史配置文件加载异常");
                console.log(err);
            })

            axios
                .get("https://raw.githubusercontent.com/Q-Tai-mu/omega-ai-2/main/public/hotsearch.json").then((resp) => {
                this.hotSearchs = resp.data["hotSearch"];

            }).catch((err) => {
                this.$Message.error("热搜配置文件加载异常");
                console.log(err);
            })
        },
        methods: {
            ...mapMutations(["SET_HURL"]),
            search() {
                //存储到历史json
                this.hurl = this.searchValue;
                this.searchForState = 'off';
            },
            minWindow() {
                // ipcRenderer.send("minWindow");
                ipcRenderer.send("minWindow");
            },
            maxWindow() {
                ipcRenderer.send("maxWindow");
                //  this.$Message.info('目前应用布局，暂不支持最大化')
            },
            closeWindow() {
                ipcRenderer.send("closeWindow");
            },
            getFocus() {
                this.searchForState = 'on';
            },
            loseFocus() {
                setTimeout(() => {
                    this.searchForState = 'off';
                }, 1000);
                //
            },
            selectedHistory(val) {
                this.searchForState = 'off';
                this.hurl = val;
                this.searchValue = val;
            }
        },
    };
</script>

